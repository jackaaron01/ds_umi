# =============================================================================
# recorder/ —— HDF5 录制器 + LeRobot 格式转换
# =============================================================================
# 职责：
#   1. 同步录制多模态数据：
#      - 图像流（相机 topic）
#      - 关节状态（/robot/joint_state）
#      - 夹爪状态（/robot/gripper_state）
#      - 遥操作指令（/teleop/joint_commands）
#   2. 时间对齐：所有数据统一到同一时间基准，误差 <5ms
#   3. 数据存储：内部使用 HDF5 格式
#   4. 格式转换：导出为 LeRobot 格式（HDF5 + Parquet），直接接入训练框架
#
# 为什么用 HDF5？
#   - 支持分层存储（一个文件包含所有 sensor 数据）
#   - 支持压缩（图像数据压缩率很高）
#   - 支持随机读取（训练时不需要顺序读整个文件）
#   - 跨语言（Python/C++/MATLAB 都能读）
#   - LeRobot 原生支持 HDF5
#
# 时间同步方案：
#   1. 所有 ROS2 消息自带时间戳（基于系统时钟）
#   2. 录制时按时间戳分桶对齐
#   3. 后续阶段可以引入硬件触发（相机 external trigger）
# =============================================================================
