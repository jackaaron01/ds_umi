# safety/ —— 安全守护节点
# =============================================================================
# 职责：
#   1. 关节限位监控：确保关节角度不超过 xArm6 的硬件限位
#   2. 速度/力矩异常检测：检测关节速度或力矩突变（可能是碰撞）
#   3. 紧急停止：在检测到异常时立即切断运动指令
#   4. 安全区域：定义末端执行器允许的工作空间范围（虚拟围栏）
#
# 设计原则：
#   - 安全节点独立于控制节点运行，不共享进程
#   - 安全节点的停止指令优先级最高，覆盖所有其他节点
#   - 即使通信中断，安全节点也能独立触发停止（超时机制）
#
# 实现方式：
#   - 订阅 /teleop/joint_commands，在转发给 robot 前做安全检查
#   - 订阅 /robot/joint_state，监控实时状态
#   - 通过 RobotInterface.stop() 触发紧急停止
# =============================================================================
